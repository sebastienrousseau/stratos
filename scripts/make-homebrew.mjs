#!/usr/bin/env node
// SPDX-License-Identifier: MIT
//
// Generate the Homebrew Formula for Stratos. The Formula points at the
// platform-specific single binary attached to the matching GitHub
// Release; users get a no-Node install via:
//
//     brew tap sebastienrousseau/tap     # one-time
//     brew install stratos
//
// We emit the Formula text on stdout. The release workflow attaches
// `stratos.rb` to the GitHub release; the homebrew-tap repo then
// auto-syncs it.
//
// SHA computation. Brew formulae for binaries need SHA-256 of each
// platform's artefact. Two modes:
//   - Without `--bin-dir`: emit `REPLACE_WITH_*_SHA` placeholders. The
//     `publish` job runs before `binaries`, so it cannot know the SHAs;
//     this is the first pass and produces an unusable-for-install
//     formula. The `manifests` job runs after binaries with `--bin-dir`
//     and regenerates the formula with real hashes.
//   - With `--bin-dir <dir>`: look up each binary in <dir>, hash it,
//     emit real `sha256 "<hex>"` lines. This is what users actually
//     `brew install` from.
//
// Usage:
//   node scripts/make-homebrew.mjs <version>                    # emits placeholders
//   node scripts/make-homebrew.mjs <version> --bin-dir <dir>    # emits real SHAs

import { createHash } from 'node:crypto';
import { readFile, access } from 'node:fs/promises';
import { join, resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = dirname(dirname(fileURLToPath(import.meta.url)));

const version = process.argv[2];
if (!version) {
  process.stderr.write('usage: make-homebrew.mjs <version> [--bin-dir <dir>]\n');
  process.exit(1);
}
const binDirIdx = process.argv.indexOf('--bin-dir');
const binDir = binDirIdx >= 0 ? resolve(process.argv[binDirIdx + 1]) : null;

/**
 * Compute the SHA-256 of `filename` if it exists in `--bin-dir` (or the
 * repo root as a secondary fallback). When neither yields a file, return
 * the `placeholder` so the pre-binaries pass still produces a parseable
 * Formula.
 *
 * @param {string} filename The unsuffixed binary name (e.g.
 *   `stratos-darwin-arm64`).
 * @param {string} placeholder The text to emit when the file isn't
 *   found — e.g. `REPLACE_WITH_DARWIN_ARM64_SHA`.
 * @returns {Promise<string>} A 64-char lowercase hex digest, or
 *   `placeholder`.
 */
async function fileSha(filename, placeholder) {
  const candidates = binDir ? [join(binDir, filename), join(ROOT, filename)] : [join(ROOT, filename)];
  for (const p of candidates) {
    try {
      await access(p);
      const buf = await readFile(p);
      return createHash('sha256').update(buf).digest('hex');
    } catch { /* try next */ }
  }
  return placeholder;
}

const darwinArmSha   = await fileSha('stratos-darwin-arm64',  'REPLACE_WITH_DARWIN_ARM64_SHA');
const darwinIntelSha = await fileSha('stratos-darwin-x64',    'REPLACE_WITH_DARWIN_X64_SHA');
const linuxArmSha    = await fileSha('stratos-linux-arm64',   'REPLACE_WITH_LINUX_ARM64_SHA');
const linuxIntelSha  = await fileSha('stratos-linux-x64',     'REPLACE_WITH_LINUX_X64_SHA');

const baseUrl = `https://github.com/sebastienrousseau/stratos/releases/download/v${version}`;

const formula = `# typed: false
# frozen_string_literal: true

# SPDX-License-Identifier: MIT
#
# Stratos — official command-line client for CloudCDN.
#
# Generated by stratos itself at release time. The homebrew-tap repo
# auto-syncs this file from the GitHub release.
#
# Project home: https://github.com/sebastienrousseau/stratos

class Stratos < Formula
  desc "Official command-line client for CloudCDN"
  homepage "https://github.com/sebastienrousseau/stratos"
  version "${version}"
  license "MIT"

  on_macos do
    on_arm do
      url "${baseUrl}/stratos-darwin-arm64"
      sha256 "${darwinArmSha}"
    end
    on_intel do
      url "${baseUrl}/stratos-darwin-x64"
      sha256 "${darwinIntelSha}"
    end
  end

  on_linux do
    on_arm do
      url "${baseUrl}/stratos-linux-arm64"
      sha256 "${linuxArmSha}"
    end
    on_intel do
      url "${baseUrl}/stratos-linux-x64"
      sha256 "${linuxIntelSha}"
    end
  end

  def install
    binary_name = case [OS.kernel_name, Hardware::CPU.arch].join("-")
                  when "Darwin-arm64" then "stratos-darwin-arm64"
                  when "Darwin-x86_64" then "stratos-darwin-x64"
                  when "Linux-arm64" then "stratos-linux-arm64"
                  when "Linux-x86_64" then "stratos-linux-x64"
                  end
    bin.install binary_name => "stratos"
  end

  test do
    assert_match "stratos v${version}", shell_output("#{bin}/stratos version")
  end
end
`;

process.stdout.write(formula);
