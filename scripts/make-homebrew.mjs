#!/usr/bin/env node
// SPDX-License-Identifier: MIT
//
// Generate the Homebrew Formula for Stratos. The Formula points at the
// platform-specific single binary attached to the matching GitHub
// Release; users get a no-Node install via:
//
//     brew tap sebastienrousseau/cloudcdn   # one-time
//     brew install stratos
//
// We emit only the Formula text on stdout. The release workflow
// attaches `stratos.rb` to the GitHub release; the
// `homebrew-cloudcdn` tap repo's CI then copies it in and pushes a
// commit. (Setting up the tap repo + the cross-repo PAT is a one-time
// user action; see examples/homebrew-tap-setup.md.)
//
// SHA computation. Brew formulae for binaries need SHA-256 of each
// platform's artefact. Because the binaries are produced by the
// `binaries` job *after* this `publish` job runs, we cannot know the
// SHAs in this step. Instead the Formula references the binaries by
// URL and downloads them; brew will compute SHAs at install time from
// the `sha256` lines we write here, which we populate post-hoc by the
// homebrew-tap repo's bump script.
//
// For the very first install (before the tap is fully wired), brew
// supports an unsigned/no-sha install via `brew install --no-quarantine
// <url>`. The Formula below is the canonical version.
//
// Usage:
//   node scripts/make-homebrew.mjs <version>    # emits to stdout

const version = process.argv[2];
if (!version) {
  process.stderr.write('usage: make-homebrew.mjs <version>\n');
  process.exit(1);
}

const baseUrl = `https://github.com/sebastienrousseau/stratos/releases/download/v${version}`;

const formula = `# typed: false
# frozen_string_literal: true

# SPDX-License-Identifier: MIT
#
# Stratos — official command-line client for CloudCDN.
#
# This Formula was generated by stratos itself at release time. To
# bump:
#   1. wait for the v${version} GitHub release to be live with binaries
#   2. run \`brew bump-formula-pr stratos --version=${version}\` from the
#      sebastienrousseau/homebrew-cloudcdn tap, OR run the tap's
#      automated bump workflow
#
# Project home: https://github.com/sebastienrousseau/stratos

class Stratos < Formula
  desc "Official command-line client for CloudCDN"
  homepage "https://github.com/sebastienrousseau/stratos"
  version "${version}"
  license "MIT"

  on_macos do
    on_arm do
      url "${baseUrl}/stratos-darwin-arm64"
      sha256 "REPLACE_WITH_DARWIN_ARM64_SHA"
    end
    on_intel do
      url "${baseUrl}/stratos-darwin-x64"
      sha256 "REPLACE_WITH_DARWIN_X64_SHA"
    end
  end

  on_linux do
    on_arm do
      url "${baseUrl}/stratos-linux-arm64"
      sha256 "REPLACE_WITH_LINUX_ARM64_SHA"
    end
    on_intel do
      url "${baseUrl}/stratos-linux-x64"
      sha256 "REPLACE_WITH_LINUX_X64_SHA"
    end
  end

  def install
    binary_name = case [OS.kernel_name, Hardware::CPU.arch].join("-")
                  when "Darwin-arm64" then "stratos-darwin-arm64"
                  when "Darwin-x86_64" then "stratos-darwin-x64"
                  when "Linux-arm64" then "stratos-linux-arm64"
                  when "Linux-x86_64" then "stratos-linux-x64"
                  end
    bin.install binary_name => "stratos"
  end

  test do
    assert_match "stratos v${version}", shell_output("#{bin}/stratos version")
  end
end
`;

process.stdout.write(formula);
